package sample.ItemTour;

public class ItemTour {
    String imageHotel;
    String numberTour;
    String hotelName;
    String tourPrice;

    public String getImageHotel() {
        return imageHotel;
    }

    public void setImageHotel(String imageHotel) {
        this.imageHotel = imageHotel;
    }

    public String getNumberTour() {
        return numberTour;
    }

    public void setNumberTour(String numberTour) {
        this.numberTour = numberTour;
    }

    public String getHotelName() {
        return hotelName;
    }

    public void setHotelName(String hotelName) {
        this.hotelName = hotelName;
    }

    public String getTourPrice() {
        return tourPrice;
    }

    public void setTourPrice(String tourPrice) {
        this.tourPrice = tourPrice;
    }
}
